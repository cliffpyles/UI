#!/usr/bin/env node
/**
 * Scans every spec under design/components/<tier>/<Name>.md, parses the
 * `uses:` frontmatter list, and writes design/components/INDEX.md with the
 * inverse `used-by` graph plus a flat inventory.
 *
 * Run after authoring or changing a spec. Safe to run any time; output is
 * deterministic.
 */
import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { join, basename } from "node:path";

const ROOT = new URL("..", import.meta.url).pathname;
const SPECS_DIR = join(ROOT, "design/components");
const TIERS = ["primitive", "base", "composite", "domain", "layout"];

function parseFrontmatter(src) {
  const match = src.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};
  const fm = {};
  for (const line of match[1].split("\n")) {
    const m = line.match(/^([a-zA-Z_-]+):\s*(.*)$/);
    if (!m) continue;
    const [, key, raw] = m;
    if (raw.startsWith("[") && raw.endsWith("]")) {
      fm[key] = raw
        .slice(1, -1)
        .split(",")
        .map((s) => s.trim().replace(/^["']|["']$/g, ""))
        .filter(Boolean);
    } else {
      fm[key] = raw.trim();
    }
  }
  return fm;
}

const specs = [];
for (const tier of TIERS) {
  const dir = join(SPECS_DIR, tier);
  let entries;
  try {
    entries = readdirSync(dir);
  } catch {
    continue;
  }
  for (const file of entries) {
    if (!file.endsWith(".md") || file.startsWith("_")) continue;
    const path = join(dir, file);
    if (!statSync(path).isFile()) continue;
    const src = readFileSync(path, "utf8");
    const fm = parseFrontmatter(src);
    const name = fm.name || basename(file, ".md");
    specs.push({
      name,
      tier: fm.tier || tier,
      level: fm.level || "",
      status: fm.status || "",
      patterns: fm.patterns || [],
      uses: fm.uses || [],
      path: `design/components/${tier}/${file}`,
    });
  }
}

specs.sort((a, b) => {
  const tierOrder = TIERS.indexOf(a.tier) - TIERS.indexOf(b.tier);
  return tierOrder !== 0 ? tierOrder : a.name.localeCompare(b.name);
});

// Build inverse index: component name → list of components that use it.
const usedBy = new Map();
for (const spec of specs) {
  for (const dep of spec.uses) {
    if (!usedBy.has(dep)) usedBy.set(dep, []);
    usedBy.get(dep).push(spec.name);
  }
}

// Render INDEX.md.
const lines = [];
lines.push("# Component Index");
lines.push("");
lines.push(
  "Generated by `scripts/generate-component-index.mjs`. Do not edit by hand."
);
lines.push("");
lines.push(`Total specs: **${specs.length}**`);
lines.push("");

lines.push("## Inventory by tier");
lines.push("");
for (const tier of TIERS) {
  const tierSpecs = specs.filter((s) => s.tier === tier);
  if (!tierSpecs.length) continue;
  lines.push(`### ${tier} (${tierSpecs.length})`);
  lines.push("");
  for (const spec of tierSpecs) {
    const patterns = spec.patterns.length ? ` _[${spec.patterns.join(", ")}]_` : "";
    const uses = spec.uses.length ? ` — uses: ${spec.uses.join(", ")}` : "";
    lines.push(`- [${spec.name}](${spec.path.replace("design/components/", "")})${patterns}${uses}`);
  }
  lines.push("");
}

lines.push("## Inverse index (used-by)");
lines.push("");
const usedByKeys = [...usedBy.keys()].sort();
if (!usedByKeys.length) {
  lines.push("_No `uses` relationships declared yet._");
  lines.push("");
} else {
  for (const name of usedByKeys) {
    const consumers = [...new Set(usedBy.get(name))].sort();
    lines.push(`- **${name}** ← ${consumers.join(", ")}`);
  }
  lines.push("");
}

writeFileSync(join(SPECS_DIR, "INDEX.md"), lines.join("\n"));
console.log(`Wrote ${join(SPECS_DIR, "INDEX.md")} (${specs.length} specs).`);
